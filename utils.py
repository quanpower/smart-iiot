def crc_func(units):
    _crc = 0
    for i in units:
        _crc += i
        if _crc > 255:
            _crc %= 256

    _crc = bitwise_reverse(_crc)
    _crc += 1
    if _crc == 256:
        _crc = 0

    return _crc



